"""ثوابت وأنماط المعالجة التحضيرية.

كل الأنماط مُجمّعة هنا لتجنب تكرار التجميع في كل استدعاء
"""

import re

# === نمط فصل الرسائل ===
# مُختبر على 88,211 سطر بصفر أخطاء
# U+200F = علامة اتجاه RTL (اختيارية)
# U+060C = الفاصلة العربية بين التاريخ والوقت
MSG_PAT = re.compile(r"^\u200f?\[\u200f?([\d/]+)\u060c\s*([\d:]+)\]")

# === أنماط التصنيف ===
# كشف رسائل الرحلات - تاء مربوطة أو هاء
TRIP_PAT = re.compile(r"رقم الرحل[ةه]")
# كشف رسائل الصور والفيديو
PHOTO_PAT = re.compile(r"لم يتم إدراج")

# === أحرف يونيكود للتنظيف من اسم المرسل ===
BIDI_CHARS = re.compile(r"[\u200f\u200e\u202f\u202a\u202b\u202c\u00a0~]")

# === بداية يوم العمل ===
# يوم العمل يبدأ 05:45:00 وينتهي 05:44:59 اليوم التالي
# ما قبل 05:45 = يُحسب لليوم السابق
WORK_DAY_HOUR = 5
WORK_DAY_MINUTE = 45

# === النوبات الثلاث ===
# كل نوبة تُعرّف بوقت البداية (ساعة، دقيقة)
# النوبة C تمتد عبر منتصف الليل لذلك تُعالج خاصة
SHIFTS = {
    "A": ((5, 45), (14, 0)),  # 05:45 - 14:00
    "B": ((14, 0), (22, 0)),  # 14:00 - 22:00
    "C": ((22, 0), (5, 45)),  # 22:00 - 05:45 (عبر منتصف الليل)
}

# === فاصل بين الحافلات في ملف المخرجات ===
SEPARATOR = "-" * 51
